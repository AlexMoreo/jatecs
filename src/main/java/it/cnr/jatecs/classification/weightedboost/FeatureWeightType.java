package it.cnr.jatecs.classification.weightedboost;

public enum FeatureWeightType {
    /**
     * Each feature has a maximum weight of 1.
     */
    MAX_THEORETICAL,

    /**
     * Each feature has a maximum weight in general different by each other
     * and computed over the feature weights of the documents.
     */
    MAX_REAL,

    /**
     * Each feature has a max weight corresponding to max value among all the features
     * weight in the index.
     */
    ABSOLUTE_MAX_REAL
}
